def raw_requests(raw,real_host=None,**kwargs):
    raw=raw.strip()
    try:
        index = raw.index('\n')
    except ValueError:
        raise Exception("ValueError")
    scheme = 'http://'
    port = 80
    try:
        method, path, protocol = raw[:index].split(" ")
    except:
        raise Exception("Protocol format error")
    raw = raw[index + 1:]

    try:
        host_start = raw.index("Host: ")
        host_end = raw.index('\n', host_start)

    except ValueError:
        raise ValueError("Host headers not found")
    if real_host:
        host = real_host
        if ":" in real_host:
            host, port = real_host.split(":")
    else:
        host = raw[host_start + len("Host: "):host_end]
        if ":" in host:
            host, port = host.split(":")
    raws = raw.splitlines()
    headers = {}
    index = 0
    for r in raws:
        if r == "":
            break
        try:
            k, v = r.split(": ")
        except:
            k = r
            v = ""
        headers[k] = v
        index += 1
    headers["Connection"] = "close"
    if len(raws) < index + 1:
        body = ''
    else:
        body = '\n'.join(raws[index + 1:]).lstrip()

    url=scheme+host+path
    if body:
        return request('post',url,headers=headers,**kwargs)
    return request(method,url,headers=headers,**kwargs)